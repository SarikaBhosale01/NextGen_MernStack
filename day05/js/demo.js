// Define variables for Principal, Rate, and Time
let principal = 1000; // Example value for Principal
let rate = 5;         // Example value for Rate (in percentage)
let time = 2;         // Example value for Time (in years)

// Calculate PTR/100
let result = (principal * rate * time) / 100;

// Output the result
console.log(`The result of PTR/100 is: ${result}`);


